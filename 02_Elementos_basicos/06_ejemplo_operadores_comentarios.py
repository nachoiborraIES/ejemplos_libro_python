'''
Programa que muestra el uso de los distintos
operadores básicos sobre tipos simples
'''

# Operadores aritméticos
print(24 + 4.25)
print(8 - 3)
print(8 / 3)
print(8 // 3)
print(8 * 3)
print(2 ** 4)   # Operador potencia. Resultado = 16
print(8 % 3)

# Operadores de asignación
numero = 4
numero += 2
print(numero)
numero *= 5
print(numero)
numero /= 3
print(numero)

# Operadores relacionales
print(5 > 3)
print(4 <= 4)
print(3 == 5)
print(5 != 3)